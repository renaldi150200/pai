      <h2 id="hukum"> Sarana Untuk Bersuci </h2>
      <p>&nbsp;&nbsp;<b>1. Air </b><br>
        &nbsp;&nbsp;Ditinjau dari hukumnya air dibagi menjadi empat <br>
        &nbsp;&nbsp;&nbsp;&nbsp; a. Air Mutlak, yaitu air suci yang dapat dipakai mensucikan, karena belum berubah sifat (warna, rasa dan bau) nya.<br>
        &nbsp;&nbsp;&nbsp;&nbsp; b. Air Musyammas, yaitu air suci yang dapat dipakai mensucikan, namun makruh digunakan. Misalnya air bertempat di logam yang bukan emas dan terkena panas matahari<br>
        &nbsp;&nbsp;&nbsp;&nbsp; c. Air Musta‟mal, yaitu air suci tetapi tidak dapat dipakai untuk mensucikan karena sudah dipakai untuk bersuci, meskipun air tersebut tidak berubah warna, rasa dan baunya.<br>
        &nbsp;&nbsp;&nbsp;&nbsp; d. Air Mutanajis, yaitu air yang terkena najis, dan jumlahnya kurang dari dua kulah (216 liter). Karenanya  air tersebut tidak suci, dan tidak dapat dipakai mensucikan. Akan tetapi jika lebih dari dua kulah serta tidak berubah warna, rasa dan baunya, maka bisa digunakan untuk bersuci.<br>
        <br>&nbsp;&nbsp;Air yang dipakai untuk bersuci adalah air mutlak yaitu air yang suci dan mensucikan. Yang tergolong air mutlak ada tujuh macam :<br>
        <br>&nbsp;&nbsp;•	Air Hujan, “(Ingatlah), ketika Allah menjadikan kamu mengantuk sebagai suatu penenteraman daripada- Nya, dan Allah menurunkan kepadamu hujan dari langit untuk mensucikan kamu dengan hujan itu dan menghilangkan dari kamu gangguan-gangguan syaitan dan untuk menguatkan hatimu dan memperteguh dengannya telapak kaki(mu)” (QS. Al Anfal: 11) <br>
        <br>&nbsp;&nbsp;•	Air Laut, Rasulullah SAW bersabda, “Air laut itu suci mensucikan, dan bangkainya juga halal”. (HR. Lima Ahli Hadist)<br>
        <br>&nbsp;&nbsp;•	Air Sumur, Air sungai, dan Air mata air,
Berdasarkan   sabda   Rasulullah   SAW,   ketika   beliau   ditanya   tentang   air   sumur   Budho‟ah,   beliau menjawab :“Air sumur itu suci, tidak dinajisi oleh sesuatu apapun” (HR. Ahmad dan Tirmidzi)
<br>
        <br>&nbsp;&nbsp;•	Air salju yang sudah mencair dan Air embun
Dasar hukum air salju dan embun adalah sabda Rasulullah SAW, ada pada doa Iftitah:
“.........Ya Allah, basuhlah aku dari segala kesalahanku dengan air salju dan air embun. (HR. Bukhari Muslim)
 <br>
        <br><br>&nbsp;&nbsp;Termasuk air mutlak adalah air yang mengalami perubahan tetapi tidak menghilangkan sifat suci men- sucikan, baik perubahan itu terjadi pada warna, rasa dan bau atau salah satu diantaranya. Air tersebut adalah: <br>
        “Apabila salah seorang dari kalian meninggal dunia, maka janganlah kalian          membiarkannya. Dan percepatlah proses penguburannya.” (HR Thabrani dari Ibnu Umar) <br>
        <br>&nbsp;&nbsp;•	Air yang berubah karena tempatnya, seperti air yang tergenang atau mengalir di tempat yang mengandung belerang. <br>
        <br>&nbsp;&nbsp;•	Air yang berubah karena lama tergenang, seperti air kolamAir yang berubah karena sesuatu yang terjadi padanya, seperti ikan yang ada di dalamnya <br>
        <br>&nbsp;&nbsp;•	Air yang berubah karena tanah yang suci atau daun-daun yang jatuh dari pohon yang berdekatan <br>
        <br>Ada satu macam air lagi yang suci dan dapat digunakan untuk mensucikan, namun haram dipakai yaitu air yang diperoleh dengan cara ghasab (yakni mengambil tanpa izin pemiliknya atau mencuri).
        
        <br><br>&nbsp;&nbsp;<b>2. Tanah, pasir, batu, dan debu yang suci </b><br>
        <br>&nbsp;&nbsp;Tanah dijadikan sebagai alat thaharah jika tidak ada air, atau tidak bisa menggunakan air karena sakit, atau karena sebab lain. Allah berfirman, “…kemudian kalian tidak mendapatkan air, maka bertayamumlah kalian dengan tanah yang suci,” (QS An Nisa : 43). <br>
        <br>&nbsp;&nbsp;Rasulullah SAW bersabda, “Dijadikan bumi itu sebagai masjid dan suci bagiku,” (HR.Ahmad). Rasulul- lah SAW juga bersabda, “Sesungguhnya tanah yang baik (bersih) adalah alat bersuci seorang muslim, ken- dati ia tidak mendapatkan air selama sepuluh tahun. Jika ia mendapatkan air, maka hendaklah ia menyen- tuhkannya ke kulit,” (HR. Tirmidzi).<br>
        <br>&nbsp;&nbsp;Di riwayat yang lain, “Rasulullah SAW mengizinkan Amr bin Ash RA bertayamum dari jinabat pada ma- lam yang sangat dingin, karena ia mengkhawatirkan keselamatan dirinya jika ia mandi dengan air yang din- gin.” (HR. Bukhari).<br>
    </p>
