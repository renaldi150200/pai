      <h2 id="sholat">Najis</h2>
      Najis adalah kotoran yang wajib untuk dihilangkan dan dibasuh.<br>
      Menurut tingkatannya najis dibagi tiga:<br>
      &nbsp;&nbsp;1.	Najis Mukhoffafah (ringan) adalah air kencing bayi laki-laki yang belum berumur dua tahun, dan belum makan sesuatu kecuali air susu ibunya. Cara menghilangkannya cukup diperciki air pada tempat yang terkena najis tersebut.<br>
      <br>&nbsp;&nbsp;2.	Najis Mutawashitho (sedang). Yang terasuk najis ini adalah<br>
        &nbsp;&nbsp;&nbsp;&nbsp; a. Bangkai binatang darat yang mempunyai darah mengalir yang mati tanpa disembelih atau tidak dis- embelih menurut syariat islam. Termasuk juga sesuatu yang dipotong dari binatang yang masih hidup seperti telinga/ekornya. <br>
        &nbsp;&nbsp;&nbsp;&nbsp; b.	Susu, tulang dan bulu hewan yang haram dimakan<br>
        &nbsp;&nbsp;&nbsp;&nbsp; c.	Nanah<br>
        &nbsp;&nbsp;&nbsp;&nbsp; d.	Darah, kecuali hati, limpa dan darah yang tersisa pada urat binatang yang disembelih<br>
        &nbsp;&nbsp;&nbsp;&nbsp; e.	Muntahan<br>
        &nbsp;&nbsp;&nbsp;&nbsp; f.	Segala sesuatu yang keluar dari qubul dan dubur manusia dan binatang, baik yang biasa terjadi, seperti air kecil dan air besar, ataupun jarang terjadi seperti madzi dan wadi.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Madzi adalah cairan encer yang keluar ketika syahwat memburu atau ketika bercumbu rayu (dari pihak wanita disebut qadzi). Sedangkan wadi adalah cairan putih kental yang keluar setelah buang air kecil atau ketika dalam keadaan letih.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Nanah, darah, dan muntahan yang sedikit dan sulit untuk dihindarkan, maka dimaafkan.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Pada pangkuan keluarga (tapi cara ini kurang sempurna, sebab menyukarkan petugas pemandian) <br>
        &nbsp;&nbsp; <b>Najis Mutawashitho dibagi dua yaitu:</b><br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Najis „ainiyah yaitu naijs yang berwujud dan dapat dilihat, misalnya kotoran manusia atau binatang.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Najis hukmiyah yaitu najis yang tidak berwujud dan tidak terlihat, seperti bekas air kencing dan arak yang mudah mengering. Cara membersihkan najis mutawashitho cukup dibasuh tiga kali agar sifat- sifat najis (warna, rasa dan bau) nya hilang.<br>
      <br>&nbsp;&nbsp;3.    Najis Mugholladzoh (berat) adalah yang berasal dari anjing dan babi seperti air liur, daging, darah dan ko- torannya. Cara menghilangkannya, dibasuh sebanyak tujuh kali dan salah satunya dengan air yang bercam- pur dengan tanah.<br>
