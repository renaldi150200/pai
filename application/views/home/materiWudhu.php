        <h2 id="pengertian">Hadast</h2>
        <p>
        Hadast adalah suatu keadaan tidak suci yang tidak dapat dilihat, tetapi wajib disucikan untuk sahnya ibadah. Pembagian jenis hadast adalah,<br>
        <br>Hadast kecil (shughro). Penyebabnya antara lain: keluar sesuatu dari dubur atau qubul, menyentuh kulit lawan jenis yang bukan muhrimnya tanpa penghalang (menurut madzhab Imam Syafii) dan tidur nyenyak dalam keadaan tidak tetap. Cara bersuci dari hadas ini ialah dengan berwudhu.<br>
        <br>Hadast besar (kubro). Penyebabnya antara lain: keluar air mani, bersetubuh, wanita selesai haid, wanita yang baru melahirkan dan selesai masa nifasnya dan seseorang yang baru masuk islam. Cara mensucikannya adalah dengan mandi wajib.<br>

        </p>
