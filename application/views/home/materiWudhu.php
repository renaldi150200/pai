      <h2 id="caratayamum"> Tata Cara Tayamum</h2>
      <p>
      Tayamum menurut bahasa sama dengan “al qoshdu”, artinya menyengaja atau menuju. Menurut syariah, tayamum adalah mengusap muka dan dua tangan dengan debu yang suci dan dengan niat agar dapat mengerja- kan shalat atau ibadah lain (bukan berniat menghilangkan hadast). Ini adalah rukhsoh (keringanan) bagi orang yang tidak dapat memakai air dalam bersuci karena alasan tertentu.<br><br>
      Adapun dalilnya adalah surah An-Nisa:43 (artinya), “Hai orang-orang yang beriman, janganlah kamu shalat, sedang kamu dalam keadaan mabuk, sehingga kamu mengerti apa yang kamu ucapkan, (jangan pula hampiri mesjid) sedang kamu dalam keadaan junub , terkecuali sekedar berlalu saja, hingga kamu mandi. Dan jika kamu sakit atau sedang dalam musafir atau datang dari tempat buang air atau kamu telah menyentuh perempuan, kemudian kamu tidak mendapat air, maka bertayamumlah kamu dengan tanah yang baik (suci). sapulah mukamu dan tanganmu. Sesungguhnya Allah Maha Pema'af lagi Maha Pengampun.”<br><br>
      Adapun dalil dari hadist Rasulullah SAW, dari Abu Umamah Al Bahli RA Nabi SAW, bersabda, “Telah dijadikan bumi untukku sebagai masjid dan alat bersuci, maka dimanapun seorang dari umatku menemui waktu shalat, maka disitu terdapat alat untuk bersuci,”. (HR. Ahmad).<br><br>
      &nbsp;&nbsp; a.	Syarat Tayamum
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Telah masuk waktu sholat. Tayamum tidak sah bila dilakukan sebelum waktu shalat dan dikhawatirkan bila menunggu adanya air akan habis waktu sholat.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Mencari air terlebih dahulu.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Tidak ada penghalang yang menghalangi sampainya media debu yang diusapkan pada anggota taya- mum seperti minyak dan pasta.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Adanya udzur yang disebabkan oleh salah satu
      <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	Bila tidak ada air, atau ada air tapi tidak mencukupi untuk bersuci.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	Tidak bisa menggunakan air karena adanya sebab dari beberapa sebab syar‟i.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;•	Ketika ada air yang jaraknya dekat tetapi untuk mengambilnya dikhawatirkan keselamatan jiwa atau hartanya.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Karena airnya sangat dingin dan di khawatirkan akan terjadi kemudaratan bila memakainya.
      <br><br>&nbsp;&nbsp; b. Rukun-rukun tayamum
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Niat.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Menyapu muka dengan tanah.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Menyapu kedua tangan (ada yang berpendapat hingga siku, ada pula pendapat hingga pergelangan tan- gan saja).
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Tertib atau berurutan.
      <br><br>&nbsp;&nbsp; c. Sunnah tayamum
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Mengawali dengan bacaan Basmalah.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Mendahulukan anggota tayamum sebelah kanan.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Bersiwak.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Meniup debu di telapak tangan agar menjadi tipis.
      <br><br>&nbsp;&nbsp; d. Sunnah tayamum
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Segala yang membatalkan wudhu juga membatalkan tayamum.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Mendapatkan air sebelum sholat.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Murtad.
      <br><br>&nbsp;&nbsp; e.	Tata cara tayamum sesuai tuntunan Rasulullah
      <br>&nbsp;&nbsp;&nbsp;&nbsp;“Ammir bin yasir berkata, “Nabi SAW mengutusku untuk suatu kepentingan. Lalu ditengah perjalanan aku junub, sedangkan aku tiidak mendapatkan air untuk bersuci. Maka aku pun berguling-guling di tanah se- bagaimana hewan berguling-guling. Kemudian aku mendatangi Nabi SAW dan kuceritakan hal tersebut kepada beliau, beliau pun berkata (yang artinya), „Sebenarnya cukup bagimu untuk bersuci dari junub itu dengan melakukan hal ini‟, kemudian beliau memukulkan kedua telapak tangan pada tanah dengan sekali pu- kulan lalu mengibaskannya. Kemudian mengusap punggung telapak tangannya dengan tangan kirinya dengan telapak tangannya. Kemudian beliau mengusap wajahnya dengan kedua tangannya” (HR. Bukhori & Muslim)
