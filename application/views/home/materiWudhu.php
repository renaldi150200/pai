      <h2 id="tharah">Thaharah dari Hadast</h2>
      <p>
      &nbsp;&nbsp;<b>1.	Wudhu</b><br>
      &nbsp;&nbsp;Perintah wajib wudhu ini turun bersama dengan perintah wajib shalat. Firman Allah SWT, “Hai orang- orang yang beriman, apabila kamu hendak mengerjakan sholat maka basuhlah mukamu, kedua tanganmu sampai siku dan sapulah kepalamu serta basuhlah kedua kakimu sampai mata kaki,” (QS. Al Maidah: 6).
      &nbsp;&nbsp;<br>Sabda Rasulullah SAW, „Allah SWT tidak akan menerima sholat seseorang yang berhadast sehingga ia berwudhu.” (HR. Abu Daud).
      
      <br>&nbsp;&nbsp; a.	Syarat Wudhu ada 5 (lima)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Islam
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Baligh
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Tidak berhadast besar
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Memakai air yang mutlak (suci dan mensucikan)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;5.	Tidak ada yang menghalangi sampainya air ke kulit, seperti getah & cat
      <br><br>&nbsp;&nbsp; b.	Rukun Wudhu ada 6 (enam)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Niat.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;"Sesungguhnya amal perbuatan itu hanya tergantung dari niat. Dan sesungguhnya, setiap orang hanya akan mendapatkan balasan sesuai dengan apa yang diniatkannya...” (HR. Bukhori & Muslim)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Membasuh muka sebatas dari tempat tumbuh rambut di kepala sampai kedua tulang dagu dan dari ba- tas telinga kanan sampai batas telinga kiri
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Membasuh kedua tangan sampai kedua mata siku
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Mengusap sebagian kepala
      <br>&nbsp;&nbsp;&nbsp;&nbsp;5.	Membasuh kedua kaki sampai mata kaki
      <br>&nbsp;&nbsp;&nbsp;&nbsp;6.	Tertib atau beraturan
      <br><br>&nbsp;&nbsp; c.	Sunnah Wudhu ada 16 (enam belas)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Diawali dengan membaca basmalah.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Membasuh telapak tangan sampai pergelangan
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Berkumur-kumur (Madhmadhah)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Bersiwak (menggosok gigi)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;5.	Menghirup air ke lubang hidung (Istinsyaq) dan mengeluarkannya dengan keras (istinsyar).
      <br>&nbsp;&nbsp;&nbsp;&nbsp;6.	Mengusap kedua telinga luar dan dalam
      <br>&nbsp;&nbsp;&nbsp;&nbsp;7.	Menyela-nyela jemari tangan dan kaki
      <br>&nbsp;&nbsp;&nbsp;&nbsp;8.	Tidak berbicara
      <br>&nbsp;&nbsp;&nbsp;&nbsp;9.	Mendahulukan membasuh anggota wudhu bagian kanan
      <br>&nbsp;&nbsp;&nbsp;&nbsp;10.	Membasuh anggota wudhu sampai tiga kali, kecuali kepala hanya satu kali.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;11.	Membaca doa setelah berwudhu (lihat gambar).
      <br>&nbsp;&nbsp;&nbsp;&nbsp;12.	Muwalat / berturut-turut yaitu tidak diselingi dengan perbuatan lain
      <br>&nbsp;&nbsp;&nbsp;&nbsp;13.	Menggosok-gosok anggota wudhu agar lebih bersih
      <br>&nbsp;&nbsp;&nbsp;&nbsp;14.	Tidak diseka, kecuali ada hajat, seperti kedinginan
      <br>&nbsp;&nbsp;&nbsp;&nbsp;15.	Tidak minta pertolongan orang lain dalam melakukan wudhu kecuali ada halangan seperti sakit
      <br>&nbsp;&nbsp;&nbsp;&nbsp;16.	Tidak berlebih-lebihan menggunakan air
      <br><br>
      &nbsp;&nbsp; d.	Perkara yang dapat membatalkan wudhu ada 6 (enam)
      <br>&nbsp;&nbsp;&nbsp;&nbsp;1.	Keluar sesuatu dari dua pintu (kubul dan dubur) atau salah satu dar keduanya baik berupa air besar, air kecil, angin, air mani atau yang lainnya.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;2.	Hilangnya akal karena gila, pingsan ataupun mabuk.
      <br>&nbsp;&nbsp;&nbsp;&nbsp;3.	Bersentuhan kulit laki-laki dewasa dengan kulit perempuan dewasa kecuali mereka itu masih muhrim
      <br>&nbsp;&nbsp;&nbsp;&nbsp;4.	Menyentuh kemaluan atau pintu dubur dengan batin telapak tangan, baik milik sendiri maupun milik orang lain, baik dewasa maupun anak-anak
      <br>&nbsp;&nbsp;&nbsp;&nbsp;5.	Tidur, kecuali apabila tidurnya dengan duduk dan masih dalam keadaan semula (tidak berubah kedudukannya), dimana dubur tertutup selama tidur
      <br>&nbsp;&nbsp;&nbsp;&nbsp;6.	Memakan daging unta. Karena ketika Rasulullah SAW ditanya: “Apakah kami harus berwudhu karena makan daging unta? Nabi menjawab :”ya” (HR. Muslim).
      <br><br>
      &nbsp;&nbsp;e.	Tata cara berwudhu sesuai tuntunan Rasulullah SAW<br>
        &nbsp;&nbsp; Diriwayatkan bahwa Utsman RA minta air lalu berwudhu. Beliau membasuk kedua telapak tangannya tiga kali lalu berkumur dan mengeluarkan air dari hidung. Kemudian membasuh wajahnya tiga kali lalu berkumur dan mengeluarkan air dari hidung. Kemudian membasuh wajahnya tiga kali. Lantas membasuh tan- gan kanannya sampai siku tiga kali. Tangan kirinya juga begitu. Setelah mengusap kepalanya, kemudian mem- basuh kaki kanannya sampai mata kaki tiga kali. Begitu juga kaki kirinya. Kemudian berkata “Aku pernah melihat Rasulullah SAW berwudhu seperti wudhuku ini. Lalu beliau bersabda, “Barangsiapa yang berwudhu seperti wudhuku ini, lalu shalat dua rakaat, dimana dalam dua rakaaat itu ia tidak berbicara dengan hatinya sendiri, maka dosanya yang telah lalu akan diampuni.”<br> 
