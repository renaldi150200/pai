      <h2 id="rukun">Thaharah dari Najis</h2>
      <p>
      &nbsp;&nbsp;<b>1.	Istinja</b><br>
      &nbsp;&nbsp;Bersuci setelah buang air kecil atau air besar dinamakan istinja. Istinja menurut bahasa artinya terlepas atau selamat, sedangkan menurut istilah syariah adalah bersuci sesudah buang air besar atau buang air kecil.
      &nbsp;&nbsp;<br>Hukum istinja adalah wajib, dan bagi yang tidak melakukannya maka berdosa. Hal ini disandarkan pada sebuah hadist. 
      &nbsp;&nbsp;<br>Ketika Rasulullah SAW melewati dua kubur, beliau bersabda, “Dua orang yang ada dalam kubur disiksa. Yang seorang disiksa karena mengadu-adu orang, dan yang seorang lagi karena tidak bersuci dari kencingnya.” (Sepakat Ahli Hadist).
      
      <br><br>Cara beristinja dapat dilakukan dengan salah satu dari cara berikut:
      <br>&nbsp;&nbsp; a.	Membasuh dan membersihkan tempat keluar kotoran dengan air sampai bersih. Ukuran bersih ini ditentukan oleh keyakinan masing-masin
      <br>&nbsp;&nbsp; b.	Membasuh atau membersihkan tempat keluar kotoran dengan batu. Kemudian dibasuh dan diber- sihkan dengan air.
      <br>&nbsp;&nbsp; c.	Membersihkan tempat keluar kotoran dengan batu atu benda-benda kesat lainnya sampai bersih. Member- sihkan tempat keluar kotoran sekurang-kurangnya dengan tiga buah atau sebuah batu yang memiliki tiga permukaan sampai bersih.
      <br><br>
      &nbsp;&nbsp;Syarat-syarat istinja‟ dengan menggunakan batu atau benda keras/kesat terdiri dari enam macam:
      <br>&nbsp;&nbsp;&nbsp;&nbsp;a.	Batu atau benda itu kesat dan harus suci serta dapat dipakai untuk membersihkan najis
      <br>&nbsp;&nbsp;&nbsp;&nbsp;b.	Batu atau benda itu tidak termasuk yang dihormati seperti bahan makanan atau batu masjid
      <br>&nbsp;&nbsp;&nbsp;&nbsp;c.	Sekurang-kurangnya dengan tiga kali usapan sampai bersih
      <br>&nbsp;&nbsp;&nbsp;&nbsp;d.	Najis yang dibersihkan belum sampai kering
      <br>&nbsp;&nbsp;&nbsp;&nbsp;e.	Najis itu tidak pindah dari tempatnya
      <br>&nbsp;&nbsp;&nbsp;&nbsp;f.	Najis itu tidak bercampur dengan benda lain, meskipun benda itu suci dan tidak terpercik oleh air
      <br><br>
      &nbsp;&nbsp;<b>Adab Buang Air </b> <br>
        &nbsp;&nbsp; a.	Mendahulukan kaki kiri pada waktu masuk temat buang air (WC)<br>
        &nbsp;&nbsp; b.	Membaca doa masuk WC<br>
        &nbsp;&nbsp; c.	Mendahulukan kaki kanan ketika keluar dari WC<br>
        &nbsp;&nbsp; d.	Membaca doa ketika keluar dari WC<br>
        &nbsp;&nbsp; e.	Pada waktu buang air hendaknya menggunakan alas kaki<br>
        &nbsp;&nbsp; f.	Istinja hendaknya dilakukan dengan tangan kiri<br> 
      <br>
      &nbsp;&nbsp;<b>Hal-hal yang dilarang ketika buang air</b> <br>
        &nbsp;&nbsp; a.	Buang air ditempat terbuka<br>
        &nbsp;&nbsp; b.	Buang air di air yang tenang<br>
        &nbsp;&nbsp; c.	Buang air di lubang-lubang karena kemungkinan ada binatang yang terganggu di dalam lubang itu.<br>
        &nbsp;&nbsp; d.	Buang air ditempat yang dapat menganggu orang lain<br>
        &nbsp;&nbsp; e.	Buang air dibawah pohon yang sedang berbuah, sebab jika buah itu jatuh akan terkena najis dan buah yang bernajis haram dimakan atau dijual<br>
        &nbsp;&nbsp; f.	Bercakap-cakap kecuali terpaksa<br>
        &nbsp;&nbsp; g.	Menghadap atau membelakangi kiblat, kecuali dalam tempat khusus dan tertutup<br>
        &nbsp;&nbsp; h.	Membawa ayat-ayat Al Qur‟an<br>
        &nbsp;&nbsp; i.	Buang air sambil berdiri, karena bertentangan dengan kesopanan dan adab yang baik<br>
        &nbsp;&nbsp; j.	Membersihkan kotoran dengan tangan kanan, karena tangan kanan untuk makan, minum, berpakaian dan perbuatan-perbuatana baik. (HR. Ahmad, Abu Daud, Ibnu Majah, Ibnu Hiban, Hakim dan Baihaqi)<br>
        <br>
        &nbsp;&nbsp;<b>2.	Memercikkan Air</b><br>
        &nbsp;&nbsp;Bersuci dari Najis Mukhoffaf (ringan), cukup dengan dipecikkan air saja pada bagian yang terkena najis kecil mukhaffaf
      <br><br>
        &nbsp;&nbsp;<b>3.	Mencuci dan membasuh dengan air</b><br>
        &nbsp;&nbsp;Bersuci dari Najis Mutawasithoh (sedang), cukup dibasuh tiga kali dengan air yang mengalir agar sifat-sifat najis (yakni warna, rasa dan bau) nya hilang)
        <br><br>
        &nbsp;&nbsp;<b>4.	Menyamak</b><br>
        &nbsp;&nbsp;Bersuci dari Najis Mugholazzoh (berat), cukup dengan dicuci tujuh kali dengan air mutlak dan salah satunya dengan air tanah
      </p>
