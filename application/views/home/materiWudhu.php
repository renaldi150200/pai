      <h2 id="pengertian">Pengertian dan Dalil Thaharah</h2>
      <p>
        Pengertian  Thaharah  secara  etomologis  adalah  „kebersihan‟  atau  „kesucian‟.  
        Menurut  terminologi syariah, Thaharah memiliki arti menghilangkan hadast dan najis dengan alat tertentu yang di tetapkan oleh syara‟. 
        Bersuci dari najis berlaku pada badan, pakaian, tempat, atau benda-benda lain yang terkena najis, sedangkan bersuci dari hadast hanya berlaku pada badan saja dengan cara wudhu, mandi besar atau tayamum sebagai pengganti wudhu dan mandi.
        <br><b>Thaharah hukumnya wajib berdasarkan Al Qur‟an dan Sunnah: </b>
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; 1.	Allah  ta‟ala  berfirman:  “Hai  orang-orang  yang  beriman,  apabila  kalian  hendak  mengerjakan shalat, maka basuhlah muka kalian dan tangan kalian sampai dengan siku, dan sapulah kepala kalian, dan (basuh) kaki kalian sampai dengan kedua mata kaki” (QS. Al Maidah:6) <br>
        &nbsp;&nbsp;&nbsp;&nbsp; 2.	Allah juga berfirman,“... dan jika kamu junub hendaklah bersuci ..”(QS. Al maidah : 6)<br>
        &nbsp;&nbsp;&nbsp;&nbsp; 3.	Allah berfirman, “ ...Maka pakaianmu bersihkanlah...” (QS. Al Mudatssir : 4)<br>
        &nbsp;&nbsp;&nbsp;&nbsp; 4.	Allah juga berfirman, “ Sesungguhnya Allah menyukai orang-orang yang tobat dan menyukai orang-orang yang menyucikan diri” (QS. Al Baqarah;222)<br>
        &nbsp;&nbsp;&nbsp;&nbsp; 5.	Rasulullah SAW bersabda, “Kunci shalat adalah bersuci”, dan sabdanya: ”Shalat tanpa wudhu tidak diterima.” (HR.Muslim)<br>
        &nbsp;&nbsp;&nbsp;&nbsp; 6.	Rasulullah SAW, bersabda. “Kesucian adalah setengah iman.” (HR. Muslim)<br>  
    </p>
