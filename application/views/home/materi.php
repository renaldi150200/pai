<br>&nbsp;&nbsp;8.	Menghadap kiblat,
 
</p>
      <h2 id="rukun">Rukun Sholat</h2>
      <p>
      &nbsp;&nbsp;1.	Niat
      <br>&nbsp;&nbsp;Niat berarti menyengaja untuk sholat, menghambakan diri kepada Allah SWT semata, serta menguat- kannya dalam hati. Rasulullah SAW bersabda: “Semua amal tergantung pada niatnya dan setiap orang akan mendapat (balasan) sesuai dengan niatnya”. (HR. Bukhori dan lain-lain. Baca Al Irwa‟, hadits no.22).
      <br>&nbsp;&nbsp; 2.	Takbiratul ihram
      <br>&nbsp;&nbsp; Takbiratul ihram yaitu takbir yang pertama ketika diucapkan oleh orang yang mengerjakan sholat se- bagai tanda mulai mengerjakan sholat dengan lafadz (ucapan) Allahu Akbar (Allah Maha Besar).
      <br>&nbsp;&nbsp; 3.	Berdiri bagi yang mampu
      <br>&nbsp;&nbsp; 4.	Membaca Surah Al Fatihah
      <br>&nbsp;&nbsp; 5.	Ruku‟ dengan thu‟maninah (tenang dan berhenti sejenak)
      <br>&nbsp;&nbsp; 6.	I‟tidal dengan thu‟maninah
      <br>&nbsp;&nbsp; 7.	Sujud dua kali dengan thu‟maninah
      <br>&nbsp;&nbsp; 8.	Duduk di antara dua sujud dengan thu‟maninah
      <br>&nbsp;&nbsp; 9.	Duduk dengan thu‟maninah,
      <br>&nbsp;&nbsp; 10.	Membaca tasyahud akhir.
      <br>&nbsp;&nbsp; 11.	Membaca sholawat Nabi.
      <br>&nbsp;&nbsp; 12.	Mengucapkan salam
      <br>&nbsp;&nbsp; 13.	Tertib (melakukan rukun secara berurutan). Sabda Rasulullah: “Shalatlah kalian sebagaimana kalian melihatku sholat” (HR. Bukhori).
</p>
      <h2 id="sunah">Sunnah-Sunnah Sholat</h2>

      <p>
      Sholat mempunyai beberapa sunnah yang dianjurkan untuk kita kerjakan sehingga menambah pahala kita menjadi banyak, diantaranya:
<br>&nbsp;&nbsp;1.	Mengangkat kedua tangan sehingga sama tinggi ujung jari dengan telinga dan telapak tangan setinggi bahu serta keduanya menghadap kiblat pada keadaan sebagai berikut:
<br>&nbsp;&nbsp;a.	Ketika takbiratul ihram
<br>&nbsp;&nbsp;b.	Ketika ruku‟ ketika bangkit dari ruku‟
<br>&nbsp;&nbsp;c.	Ketika berdiri setelah rakaat kedua ke rakaat ketiga
<br>&nbsp;&nbsp;Hal ini berdasarkan hadits dari Ibnu Umar r.a: “Bahwasanya Nabi SholAllahu alaihi wasallam apa- bila beliau melaksanakan sholat, beliau mengangkat kedua tangannya sampai sejajar dengan kedua bahu beliau, kemudian membaca takbir. Apabila beliau ingin ruku‟ beliau pun mengangkat kedua tan- gannya seperti itu dan begitu pula kalau beliau bangkit dari ruku‟ beliaupun mengangkat kedua tan- gannya seperti itu dan begitu pula beliau bangkit dari sujud.” (Muttafaq‟alaih)
<br>&nbsp;&nbsp;2.	Meletakkan tangan kanan di atas tangan kiri di atas dada atau di bawah dada dan di atas pusar. Hal ini berdasarkan  perkataan Sahl bin Sa‟d r.a ;”Orang-orang (di masa Nabi ShollAllahu alaihi wasallam) disuruh untuk meletakkan tangan kanan di atas tangan kiri dalam sholat”. (HR. Bukhori secara mau- quf. Al Hafidz ibnu Hajar berkata:”Riwayat ini dihukumi marfu”).
<br>&nbsp;&nbsp;3.	Senantiasa melihat ke tempat sujud kecuali pada waktu membaca “Asyhadu alla ilaaha illAllah” (pada saat duduk membaca tasyahud) maka pada waktu itu melihat ke telunjuknya.
<br>&nbsp;&nbsp;4.	Membaca doa iftitah sesudah takbiratul ihram sebelum membaca surah Al Fatihah.
<br>&nbsp;&nbsp;5.	Membaca isti‟adzah pada rakaat  pertama dan membaca basmalah dengan suara pelan pada tiap-tiap rakaat. Hal ini berdasarkan firman Allah SWT:
<br>&nbsp;&nbsp;“Maka  apabila  kamu  membaca  Al  Qur‟an,  maka  hendaklah  kamu  memohon  kepada  Allah  dari (godaan) syaitan yang terkutuk”. (QS. An Nahl: 98).
<br>&nbsp;&nbsp;6.	Membaca “aamiin” setelah membaca surat Al Fatihah. Hal ini disunnahkan kepada setiap orang yang sholat, baik sebagai imam maupun makmum atau sholat sendirian.

      </p>
