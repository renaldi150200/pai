      <!-- Dasar Hukum -->
      <h2 id="hukum">Dasar Hukum Wajib Membaca Al Qur’an dengan Tajwid </h2>
      <p>Ada 2 dasar hukum mengenai wajibnya membaca Al Qur’an dengan tajwid, yaitu : 
        <br>
        &nbsp;&nbsp;1.	Al Qur'an,<br>
        Allah SAW berfirman dalam Al Qur’an : <br>
        <i>“...Dan bacalah Al Qur’an itu dengan tartil.” (QS. Al Muzzammil : 4) </i><br>
        Ayat ini memerintahkan kita agar membaca Al Qur’an dengan perlahan-lahan sehingga membantu pemahaman dan perenungan terhadap Al Qur’an.
        <br><br>Senada dengan maksud ayat di atas, Allah SWT berfirman : <br>
        <i>“Dan Al Qur’an itu telah Kami turunkan dengan berangsur-angsur agar kamu membacakannya perlahan-lahan kepada manusia…” (QS. Al Isro’ : 106). </i>
        <br>
        <br>
        &nbsp;&nbsp;2.	Hadist,<br>
        Dari Aisyah r.a. kepadanya pernah disampaikan bahwa ada orang yang dapat membaca Al Qur’an dalam satu malam sekali atau dua kali khatam. “Aisyah berkata : mereka merasa membaca tapi sebenarnya tidak. Aku pernah bersama Rosululloh SAW satu malam penuh, Rosululloh hanya sempat membaca Surah Al Baqarah, Ali Imran, dan An Nisaa’. Bila bertemu dengan ayat adzab, Rosululloh meneruskan bacaannya hingga beliau berdoa memohon perlindungan. Begitu pula beliau tidak meneruskan bacaan bila bertemu dengan ayat yang menggembirakan hingga beliau berdoa serta mengharapkannya.”<br>
        <br>Dari Abi Hamzah, ia berkata : “Aku pernah berkata kepada Ibnu Abbas bahwa aku membaca dengan cepat dan dapat menamatkan Al Qur’an dalam 3 hari. Ibnu Abbas menjawab : “Membaca Surah Al Baqarah semalam dengan memperhatikan isinya dan tartil lebih baik dan lebih aku senangi daripada yang engkau katakan.” <br>
        <br>Beberapa surat dan hadist di atas memerintahkan kita agar membaca Al Qur’an dengan tartil. Ini artinya secara tidak langsung kita pun dituntut untuk mempelajari ilmu tentang cara membaca Al Qur’an dengan tartil. 


      </p>
