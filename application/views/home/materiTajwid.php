      <h2 id="pengertian">Ilmu Tajwid</h2>
      <h2></h2>
      <p><b>Pengertian Tajwid</b><br> 
        Tajwid secara bahasa berasal dari kata jawwada-yujawwidu-tajwidan yang artinya membaguskan atau membuat jadi bagus. Pengertian lain menurut lughoh (bahasa), tajwid juga dapat diartikan sebagai segala sesuatu yang mendapatkan kebajikan
        <br>
        Sedangkan pengertian tajwid menurut istilah adalah ilmu yang memberikan segala pengertian tentang huruf, baik hak-hak huruf (haqqul huruf) maupun hukum-hukum baru yang timbul setelah hak-hak huruf (mustahaqqul huruf) dipenuhi, yang terdiri atas sifat-sifat huruf, hukum-hukum madd, dan sebagainya. Sebagai contoh adalah tarqiq, tafkhim, dan semisalnya.     
        <br>
        <b>Tujuan mempelajari Ilmu Tajwid</b><br>
        Agar dapat membaca ayat-ayat Al Qur’an secara benar (fasih) sesuai dengan yang diajarkan oleh Rasulullah SAW, juga agar dapat memelihara lisan dari kesalahan-kesalahan ketika membaca Kitab Allah SWT (Al Qur’an).   
      </p>
      <br>
