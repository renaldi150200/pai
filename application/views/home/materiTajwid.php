      <h2 id="rukun">Makhroj Huruf</h2>
      <p>
      Tiap-tiap huruf hijaiyyah mempunyai tempat keluarnya masing-masing dari bagian-bagian mulut tertentu. Tempat keluar huruf ini dinamakan makhroj. Makhroj huruf ini dapat dikelompokkan sebagai berikut : <br>
      &nbsp;&nbsp;1.	Kelompok huruf-huruf halqiah (tenggorokan) 
      <br>&nbsp;&nbsp; 2.	Kelompok huruf-huruf lahawiyah (tekak) 
      <br>&nbsp;&nbsp; 3.	Kelompok huruf-huruf syajariah (tengah lidah) 
      <br>&nbsp;&nbsp; 4.	Kelompok huruf-huruf asaliyah (ujung lidah)
      <br>&nbsp;&nbsp; 5.	Kelompok huruf-huruf dzalaqiyah (pinggir lidah) 
      <br>&nbsp;&nbsp; 6.	Kelompok huruf-huruf nith’iyah (langit-langit lidah) 
      <br>&nbsp;&nbsp; 7.	Kelompok huruf-huruf litsawiyah (gusi) 
      <br>&nbsp;&nbsp; 8.	Kelompok huruf-huruf syafawiyah (bibir)
      <br>
      
      <div class="col-4">
          <h5>1. Kelompok Huruf-Huruf Halqiah (Tenggorokan)</h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj1.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah hamzah, ha, kho, ‘ain, ghoin, ha’. 
      <br>Huruf hamzah dan ha’ makhrojnya di tenggorokan bagian dalam. 
      <br>Huruf ‘ain dan ha makhrojnya di tenggorokan bagian tengah. 
      <br>Huruf kho dan ghoin makhrojnya di tenggorokan bagian luar.  
      <div class="col-4">
          <h5><br>2. Kelompok Huruf-Huruf Lahawiyah (Tekak)</h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj2.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah qof dan kaf.
      <br>Huruf qof  makhrojnya   di pangkal  lidah dekat   tenggorokan, sejajar  dengan  langit-langit  lunak. 
      <br>Huruf kaf makhrojnya di pangkal lidah, sejajar dengan langit-langit lunak dan sedikit di bawah makhraj qaf.
      <div class="col-4">
          <h5><br>3. Kelompok Huruf-Huruf Syajariah (Tengah Lidah) </h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj3.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah jim, syin, dhod, dan ya. 
      <br>Huruf jim, syin, dan ya makhrojnya di lidah bagian tengah, sejajar dengan langit-langit keras bagian atas. 
      <br>Huruf dhod makhrojnya di sisi lidah, sejajar dengan geraham bagian atas. 
      <div class="col-4">
          <h5><br>4. Kelompok Huruf-Huruf Asaliyah (Ujung Lidah) </h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj4.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah za, sin, dan shod.  
      <br>Huruf zai, sin, dan shod makhrajnya di ujung lidah lewat gigi seri atas, yaitu di bawah gigi seri bawah dengan sedikit kelonggaran.  
      <div class="col-4">
          <h5><br>5. Kelompok Huruf-Huruf Dzalaqiyah (Pinggir Lidah) </h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj5.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah ro, lam, dan nun. 
      <br>Huruf lam makhrojnya di ujung lidah sejajar dengan gusi atas. 
      <br>Huruf nun makhrojnya di ujung lidah, sedikit di bawah makhroj lam.  
      <br>Huruf ro makhrojnya di ujung lidah, di bawah makhroj nun.
      <div class="col-4">
          <h5><br>6. Kelompok Huruf-Huruf Nith’iyah (Langit-langit Lidah) </h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj6.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah ta, dal, dan tho.
      <br>Huruf ta, dal, dan tho makhrajnya di ujung lidah lewat pangkal gigi seri atas. 
      <div class="col-4">
          <h5><br>7. Kelompok Huruf-Huruf Litsawiyah (Gusi) </h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj7.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah tsa, dzal, dan zho.
      <br>Huruf tsa, dzal, dan zho makhrojnya keluar dengan menempelkan ujung lidah di ujung gigi seri atas.
      <div class="col-4">
          <h5><br>8. Kelompok Huruf-Huruf Syafawiyah (Bibir) </h5>
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>makroj8.png"  class="rounded img-fluid">
      </div><br>
      Huruf-hurufnya adalah ba, fa, wau, dan mim. 
      <br>Huruf ba, wau, dan mim makhrajnya di antara dua bibir. 
      <br>Huruf fa makhrajnya di bagian dalam bibir bawah serta ujung gigi seri atas. 
</p>
     
