        &nbsp;&nbsp;<h4>2.	Mengkafani</h4><br>
        Posisi ketika Mengkafankan <br>
        <div class="col-4">
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>Janaiz3.png" class="">
      </div>
        <br>&nbsp;&nbsp;<b>Masalah yang harus diperhatikan : </b> <br>
        &nbsp;&nbsp;&nbsp;&nbsp; Sabda Rasulullah Shallallahu 'Alaihi Wa sallam “ Apabila seseorang dari kamu mengkafani saudaranya maka hendaknya baik-baik mengkafaninya”. (HR. Ahmad Muslim, Abu Daud dari Jabir r.a)  <br>
        &nbsp;&nbsp;&nbsp;&nbsp; a. Siapkan perlengkapan kafan  <br>
        &nbsp;&nbsp;&nbsp;&nbsp;    Untuk pria : ± 9 m terdiri dari 3 lapis/ helai (1 helai ± 2 1/4  meter) <br>
        &nbsp;&nbsp;&nbsp;&nbsp;    Untuk wanita : ± 12 m terdiri dari 5 lapis/ helai (termasuk baju, kain bawahan, dan kerudung/ jilbab) (disunnahkan kainnya berwarna putih)  <br>
        &nbsp;&nbsp;&nbsp;&nbsp;    Catatan : yang sedang berihram tidak pakai kafan lagi. Jangan berlebihan dalam hal kain kafan. Sediakan tikar atau sejenisnya untuk alas  <br>
        &nbsp;&nbsp;&nbsp;&nbsp; b. Kain kafan yang diwajibkan hanya selapis. <br><br>
        <!-- praktik mengkafani -->
        &nbsp;&nbsp;<b>	  Praktik Mengkafani</b><br>
        &nbsp;&nbsp;	  1.	Ukur kain kafan untuk si mayit, kain kafan dilebihkan sejengkal dari kepala dan kaki untuk memudahkan mengikat nantinya.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  a.	Jika badan mayit besar, kain kafan bisa ditambah lebarnya atau kain ditumpangkan, boleh pula dijahit jelujur.<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  b.	Potong kain untuk tali kafan 5 atau 7 buah (diletakkan paling bawah sebelum kain kafan pertama lapis lipafat)<br>
        <br>&nbsp;&nbsp;	  2.	Teknik penyusunan untuk perempuan<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  a.	Disusun mulai dari kain penutup luar (Lipafat)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  b.	Lembar kedua yang sama panjang (Lipafat)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  c.	Letakkan kain kerudung pada bagian kepala (lapis Khimyar)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  d.	Baju kurung yang sudah dilubangi tengahnya (lapis Dir‟a)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  e.	Kain bawahan/tapih/sarung (lapis Izhar)<br>
        &nbsp;&nbsp;&nbsp;&nbsp;	  d.	Letakkan kapas yang sudah diberi serbuk cendana/ harum-haruman<br><br>
        <!-- membungkus jenazah -->
        &nbsp;&nbsp;<b>	  Teknik Membungkusnya </b><br>
        &nbsp;&nbsp;<b>	  Untuk pria </b><br>
        &nbsp;&nbsp;	  1.	Membungkus jenazah selapis demi selapis.<br>
        &nbsp;&nbsp;	  2.	Selusupkan lapisan yang lagsung berhubungan dengan jenazah (lapis Izhar)<br>
        &nbsp;&nbsp;	  3.	Kemudian selusupkan lembar kedua (lapis Lipafat).<br>
        &nbsp;&nbsp;	  4.	Yang terakhir lembar ketiga sebagi selubung (lapis Lipafat).<br>
        &nbsp;&nbsp;	  5.	Ikatlah tali kafan, usahakan simpulnya berada disebelah kiri jenazah.<br>
        &nbsp;&nbsp;	  6.	Ditutup kembali dengan kain yang lain.<br>
        <br>
        &nbsp;&nbsp;<b>	  Untuk wanita </b><br>
        &nbsp;&nbsp;	  1.	Masukkan / selusupkan kain bawahannya (lapisan Izhar) lebih dahulu.<br>
        &nbsp;&nbsp;	  2.	Masukkan baju kurungnya (lapisan Dir‟a) tanpa mengangkat kepala mayit.<br>
        &nbsp;&nbsp;	  3.	Pakaikanlah kerudungnya (lapisan Khimyar), jangan menutup wajahnya, kemudian rapikan.<br>
        &nbsp;&nbsp;	  4.	Pakaikanlah kain selubungnya (lapisan Lipafat) yang nantinya akan menutup seluruh tubuhnya.<br>
        &nbsp;&nbsp;	  5.	Yang terakhir, dipasangkan kain penutup luar (lapisan Lipafat) jenazah.<br>
        &nbsp;&nbsp;	  6.	Ikatlah tali kafan, usahakan simpulnya berada disebelah kiri jenazah.<br>
        &nbsp;&nbsp;	  7.	Ditutup kembali dengan kain yang lain.<br><br>
