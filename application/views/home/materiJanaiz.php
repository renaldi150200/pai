      <h2 id="hukum"> Tata Cara Persiapan Sebelum Penyelenggaraan Jenazah </h2>
      <p>&nbsp;&nbsp;1. Dalam menghadapi beratnya penderitaan sakaratul maut hendaklah selalu : <br>
        &nbsp;&nbsp;&nbsp;&nbsp; a. Berdoa ruqyah.<br>
        &nbsp;&nbsp;&nbsp;&nbsp; b. Bersabar menyerahkan diri kepada Allah.<br>
        &nbsp;&nbsp;&nbsp;&nbsp; c. Jangan berharap cepat mati.<br>
        &nbsp;&nbsp;&nbsp;&nbsp; d. Baguskanlah prasangka kepada Allah.<br>
        <br>&nbsp;&nbsp;2.  Berwasiat, jika memiliki harta benda (QS. Al Baqarah : 180) <br>
        <br>&nbsp;&nbsp;3.	Membimbing si sakit untuk talqin kalimat tauhid <br>
        “Kamu talkinkanlah (ajarkanlah) orang-orang yang dekat mati diantara kamu dengan   :Laa ilaaha illAllah” (HR. Jamaah) <br>
        <br>&nbsp;&nbsp;4.	Menghadapkan calon mayit kearah kiblat. <br>
        “.....baitil haram, adalah kiblatmu hidup atau mati.”  (HR Abu.Dawud, Nasa’i) <br>
        <br>&nbsp;&nbsp;5.	Segera memejamkan mata dan menutup mulutnya setelah lepas ruhnya.  <br>
        “Ruh itu jika diambil/dipisahkan, akan mengikutinya mata.” (HR. Muslim) <br>
        <br>nbsp;&nbsp;6. Sebelum disholatkan, keluarga / kerabat segera melunasi hutang si mayit. <br>
        Sabda Rasulullah SAW : “ Nyawa seorang mu’min itu tergantung dengan hutangnya   sehingga melunasinya.” (HR. Ahmad) <br>
        <br>&nbsp;&nbsp;7.	Segera menyelenggarakan jenazah, jangan ditunda tanpa alasan syar‟i. <br>
        “Apabila salah seorang dari kalian meninggal dunia, maka janganlah kalian          membiarkannya. Dan percepatlah proses penguburannya.” (HR Thabrani dari Ibnu Umar) <br>
        <br>&nbsp;&nbsp;8.	Disunnahkan untuk masa berkabung (Al Ihdad) 3 hari. <br>
        (Bahwasanya Nabi SAW, memberi tangguh kepada keluarga Ja’far selama tiga hari         untuk mendatangi mereka. Kemudian beliau mendatangi mereka seraya berkata:    “janganlah kalian menangisi saudaraku setelah hari ini....” (HR. Abu Dawud) <br>
        <br>&nbsp;&nbsp;9.	Diperbolehkan menyampaikan berita kematian (An na‟yu) <br>

      </p>
