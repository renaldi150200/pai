      <h2 id="pengertian">Penyelenggaraan Jenazah</h2>
      <h2></h2>
      <b>Menurut hadist-hadist shohih, antara lain riwayat Bukhori, Muslim dan Thobroni  bahwa: </b>
        <br>
        &nbsp;&nbsp;&nbsp;&nbsp; a. Mempelajari urusan penyelenggaraan jenazah hukumnya Fardhu. <br>
        &nbsp;&nbsp;&nbsp;&nbsp; b. Sedangkan urusan praktiknya hukumnya adalah Fardhu Kifayah.<br>
        Yang dimaksud dengan fardhu kifayah adalah kewajiban yang ditujukan kepada orang banyak, tetapi apabila sebagian dari mereka telah mengerjakannya maka gugurlah kewajiban bagi yang lain, namun jika tidak ada seorang pun yang mengerjakan kewajiban itu maka mereka berdosa semua.      
        <br><br>
        <b>Ganjaran bagi orang yang menyelenggarakan, sesuai dengan sabda Rasulullah SAW: </b><br>
        “Barang siapa melayat sehingga disholatkan, maka akan mendapatkan pahala 1 qiroth, dan barang siapa melayatnya sehingga dikubur, maka akan mendapatkan 2 qiroth.. Orang bertanya, “Apakah 2 qiroth itu?”, Sahut beliau : “semisal 2 bukit yang besar.” (HR Bukhori dan Muslim dari Abu Huroiroh r.a) 
      </p>
