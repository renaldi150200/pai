      <h2 id="sholat">Tata Cara Penyelenggaraan Jenazah</h2>
      &nbsp;&nbsp;<h4>1.	Memandikan</h4><br>
        Posisi ketika Memandikan <br>
        <div class="col-4">
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>Janaiz1.png" class="">
