      &nbsp;&nbsp;<b>Masalah yang harus diperhatikan : </b> <br>
        &nbsp;&nbsp;&nbsp;&nbsp; a. Sabda Rasulullah SAW : “Laa tasubbul amwat”  (jangan menyakiti atau berlaku kasar pada si mayit). <br>
        &nbsp;&nbsp;&nbsp;&nbsp; b. “Mulailah dengan anggota tubuh yang sebelah kanan dan anggota wudhunya” (HR. Bukhori & Muslim) <br>
        &nbsp;&nbsp;&nbsp;&nbsp; c. Situasi normal jenazah laki-laki dimandikan oleh laki-laki, perempuan oleh perempuan, kecuali suami istri dan anak-anak. <br>
        &nbsp;&nbsp;&nbsp;&nbsp; d. Siapkan sarana pemandian jenazah (jangan  diletakkan dilantai) <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Batang pisang yang sudah dikeruk tengahnya agar lembut <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Tempat khusus  pemandian  <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Pada pangkuan keluarga (tapi cara ini kurang sempurna, sebab menyukarkan petugas pemandian) <br>
<br>
      <div class="col-4">
          <img src="<?=base_url('assets/vendor/asset-materi/image/');?>Janaiz2.png" class="">
      </div>
        &nbsp;&nbsp;&nbsp;&nbsp; f. Siapkan perlengkapan mandi (kain pengusap, gayung minimal 3 buah, talang/pengucur air/selang, kain basahan mayit). <br>
        &nbsp;&nbsp;&nbsp;&nbsp; g. Petugas<br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Staf pemandian minimal 2 orang (seorang petugas pengusap, dan seorang lagi petugas penyiram)  <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; - Staf pemandian yang lengkap/sempurna 5 orang, yaitu : <br>
        &nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Pemimpin, pengusap, penyiram, pembantu 2 orang (pengusap dan penyiram). <br>        
        &nbsp;&nbsp;&nbsp; Selama memandikan JANGAN BERBICARA, kecuali bacaan TAHLIL : Laa ilaaha  Illallah Laa haula wa laa quwwata illa billah. <br>
        <br>
        <br>
        &nbsp;&nbsp;<b>Praktik Memandikan  </b> <br>
        &nbsp;&nbsp;1.	Jenazah masih tertutup kain.<br>
        &nbsp;&nbsp;2.	Jenazah dibersihkan anggota badannya dari najis/kotoran dan sesuatu yang menghalangi air (termasuk qubul dan dubur).<br>
        &nbsp;&nbsp;3.	Buka kain atas sampai dada untuk laki-laki, bahu untuk wanita. <br>
        &nbsp;&nbsp;4.	Diwudlukan anggota badan jenazah.<br>
        &nbsp;&nbsp;5.	Bersihkan bagian kepala dengan air sabun (wajah, mata, lubang hidung, gigi/mulut,telinga, leher, rambut). <br>
        &nbsp;&nbsp;6.	Bersihkan bagian tubuh bagian kanan dengan air sabun (bahu, tangan, dada, perut, pinggang, pinggul, qubul, dubur, kaki).<br>
        &nbsp;&nbsp;7.	Bersihkan bagian tubuh bagian kiri dengan air sabun (bahu, tangan, dada, perut, pinggang, pinggul, qubul, dubur, kaki). <br>
        &nbsp;&nbsp;8.	Dilakukan sebanyak bilangan ganjil (minimal tiga kali) dari membersihkan bagian kepala sampai tubuh bagian kiri dengan membilas memakai air kapur barus pada bilasan terakhir. <br>
        &nbsp;&nbsp;9.	Tertib.<br><br>
        &nbsp;&nbsp;<b>Catatan:  </b> <br>
        &nbsp;&nbsp;1.	Petugas tidak boleh berhadast besar, jenazah tidak diwudhui lagi,  orang mati syahid tidak di mandikan.<br>
        &nbsp;&nbsp;2.	Tidak ada ketentuan dan contoh dari Nabi Muhammad SAW, selesai dimandikan jenazah diwudhui. (Lihat HSR. Al Jamiah dari  Ummu „Athiyah pada no.A.1.b) <br>
        &nbsp;&nbsp;	Sabda Rasulullah SAW : <br>
        &nbsp;&nbsp;	<i>“Manakah wudhu yang lebih rata daripada mandi” (HR Ibnu Abi Syaibah)</i> <br>
