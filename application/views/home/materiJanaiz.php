      <h2 id="rukun">Tata tertib mengiringi jenazah</h2>
      <p>
      &nbsp;&nbsp;a.	Mengiringi jenazah hendaknya bersikap tenang, bukan bersenda gurau atau membicarakan ma- salah duniawi lainnya. 
      <br>&nbsp;&nbsp; b.	Pengiring jenazah yang berjalan kaki boleh berada di depan, disamping kanan atau kiri keranda. “orang yang berkendaraan itu dibelakang jenazah, dan yang berjalan kaki didepannya dekat dari padanya	dari	arah	kanan	dan	kirinya.”	(HR.	Bukhori	dari	Mughiroh). Catatan : Pengiring jenazah yang berkendaraan harus berada dibelakang jenazah. (kecuali petugas keamanan lalu lintas)
      <br>&nbsp;&nbsp; c.	Wanita dicegah untuk ikut mengantarkan jenazah. “ Kami perempuan dilarang mengikuti jenazah meskipun larangan itu tidak diperkeras.” (HR. Bukhari, Muslim dari Ummu Athiyah).
      <br>&nbsp;&nbsp; d.	Sebelum   jenazah   diletakkan    dikuburan,    pengiring    jenazah    hendaklah    jangan    duduk. “ Bilamana kamu melihat jenazah maka berdirilah, maka siapa yang mengikutinya janganlah dia duduk sehingga jenazah itu diletakkan.” (HR .Jama‟ah kecuali Ibnu Majah dari Abi Said, HR. Bukhori no.682)
      <br>&nbsp;&nbsp; e.	Memasuki gerbang/wilayah perkuburan ucapkanlah salam/doa.
      <br>&nbsp;&nbsp; <i>Jangan melangkahi dan duduk diatas kuburan. Sabda Rasulullah SAW :”Niscaya seorang dari padamu duduk diatas bara api, lalu membakar pakaiannya hingga tembus kulitnya, lebih baik daripada duduk diatas kubur.” </i>(HR Abu Hurairah, diriwiyatkan oleh Jama‟ah selain Bukhary dan Tirmidzi).
      <br><br>

      &nbsp;&nbsp;<b>Beberapa prinsip penguburan: </b> <br>
        &nbsp;&nbsp; 1.	Masukanlah jenazah tersebut dari arah kaki kubur.<br>
        &nbsp;&nbsp; Dengan membaca do‟a:<i> “Bismillahi wa’ala millati rasulillah”</i><br>
        &nbsp;&nbsp; Artinya : <i>“ Dengan nama Allah dan atas (mengikuti) perilaku Rasulullah”</i><br>
        &nbsp;&nbsp; 2.	Jenazah diletakkan menghadap kiblat, miring pada rusuk kanannya.<br>
